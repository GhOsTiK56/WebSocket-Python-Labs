#!/bin/bash

# Скрипт для запуска тестов приложения "Игра в загадки"
# Запускает сервер в фоне, выполняет тесты и останавливает сервер

echo "Запуск приложения..."
python3 main.py &
APP_PID=$!

sleep 2

echo "Запуск тестов..."
pytest -q --disable-warnings tests/

echo "Остановка приложения..."
kill $APP_PID

echo "Скрипт завершён."
